package co.yedam.stream;

public class HighStudent {
	String name;
	Gender gen;
	int eScore;
	int mScore;
	double avg;
	
	public HighStudent(String name, Gender gen, int eScore, int mScore, double avg) {
		super();
		this.name = name;
		this.gen = gen;
		this.eScore = eScore;
		this.mScore = mScore;
		this.avg = avg;
	}

}
